// Variables are Containers for Storing Data
// JavaScript Variables can be declared in 4 ways:

// Automatically
// Using var
// Using let
// Using const

x = 5;
y = 6;
z = x + y;

//using let
let x = 5;
let y = 6;
let z = x + y;
//using var
var x = 5;
var y = 6;
var z = x + y;

//using const
const x = 5;
const y = 6;
const z = x + y;

//mixed
const price1 = 5;
const price2 = 6;
let total = price1 + price2;

//JavaScript Dollar Sign $
let $ = "Hello World";
let $$$ = 2;
let $myMoney = 5;

